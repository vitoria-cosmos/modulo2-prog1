# Função que formata cpf

def formata_cpf(cpf):
    cpf_formatado = f'{cpf[:3]}.{cpf[3:6]}.{cpf[6:9]}-{cpf[9:]}'
    return cpf_formatado


# função que pega o primeiro nome
def primeiro_nome(nome):
    nome_formatado = nome.split()[0]
    return nome_formatado

